//#1Implicit type coercion in javascript is automatic conversion of value from one data type to another.
// It takes place when the operands of an expression are of different data types.


//#2**Note - ‘ + ‘ operator when used to add two numbers, outputs a number. 
//The same ‘ + ‘ operator when used to add two strings, outputs the concatenated string:
var x = 3;
var y = "3";
console.log(x + y) // Returns "33"

var name = "Vivek";
var surname = " Bisht";
console.log(name + surname) //Returns "Vivek Bisht"